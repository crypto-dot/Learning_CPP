//Friend
//*A function or class that has access to a private data member
//*and that function or class is not a member of that class that it is accessing
//Function
//*Can be regular nonmember functions
//*Can be member methods of another class
//Class
//*Another class can have access to private class members
//*********************************************************
//*Friendship must be granted not taken
//*Declared in the class that is granting friendship
//*Declared in the function prototype with the keyword friend
//*Friendship is not symmetric (if A is a friend of B, B is not a friend of A)
//*Friendship is not transitive (if A is a friend of B, and B is a friend of C, then A is NOT a friend of C